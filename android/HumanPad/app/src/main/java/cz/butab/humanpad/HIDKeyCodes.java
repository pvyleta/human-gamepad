package cz.butab.humanpad;

public class HIDKeyCodes {
    /** Dec: 26 */
    public static final int Char_W = 0x1a;     // DEC: 26
    /** Dec :4 */
    public static final int Char_A = 0x04;     // DEC: 4
    /** Dec: 22 */
    public static final int Char_S = 0x16;     // DEC: 22
    /** Dec: 7 */
    public static final int Char_D = 0x07;     // DEC: 7

    /** Dec: 8 */
    public static final int Char_E = 0x08;     // DEC: 8

    /** Dec: 82 */
    public static final int Arrow_Up = 0x52;     // DEC: 82
    /** Dec: 81 */
    public static final int Arrow_Down = 0x51;     // DEC: 81
    /** Dec: 80 */
    public static final int Arrow_Left = 0x50;     // DEC: 80
    /** Dec: 79 */
    public static final int Arrow_Right = 0x4f;     // DEC: 79

    /** Dec: 44 */
    public static final int Space = 0x2c;       // DEC: 44
    /** Dec: 224 */
    public static final int LeftControl = 0xe0;     // DEC: 224
    /** Dec: 228 */
    public static final int RightControl = 0xe4;     // DEC: 228
}
